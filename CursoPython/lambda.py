print(sumar(5,60))

resta = lambda n1,n2: n1 - n2
print(resta(60,5))

multiplica = lambda n1,n2: n1 * n2
print(multiplica(60,5))

divide = lambda n1,n2: n1 / n2
print(divide(60,5))

resto = lambda n1,n2: n1 & n2
print(resto(60,5))

potencia = lambda n1,n2: n1 ** n2
print(potencia(60,5))